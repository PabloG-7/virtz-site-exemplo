function addMessageToChat(message, isUser = false) {
    const chatMessages = document.getElementById('chatMessages');
    const div = document.createElement('div');
    div.className = isUser ? 'user-msg' : 'bot-msg';

    div.style.whiteSpace = 'pre-line';
    div.textContent = message;
    chatMessages.appendChild(div);
    chatMessages.scrollTop = chatMessages.scrollHeight;
}

function sendMessage() {
    const chatInput = document.getElementById('chatInput');
    const userText = chatInput.value.trim();
    if (!userText) return;

    const chatMessages = document.getElementById('chatMessages');

    addMessageToChat(userText, true);
    chatInput.value = '';
    chatMessages.scrollTop = chatMessages.scrollHeight;

    const typingDiv = document.createElement('div');
    typingDiv.className = 'typing-indicator';
    typingDiv.innerHTML = '<span></span><span></span><span></span>';
    chatMessages.appendChild(typingDiv);
    chatMessages.scrollTop = chatMessages.scrollHeight;

    setTimeout(() => {
        typingDiv.remove();
        const response = virtzAI.processMessage(userText);
        addMessageToChat(response);

        if (virtzAI.context.mood === 'happy' && virtzAI.memory.length > 2 && Math.random() > 0.65) {
            setTimeout(() => {
                const suggestion = virtzAI.getProactiveSuggestion();
                const suggestionDiv = document.createElement('div');
                suggestionDiv.className = 'bot-msg';
                suggestionDiv.style.fontSize = '13px';
                suggestionDiv.style.color = '#555';
                suggestionDiv.style.borderColor = '#f6df57';
                suggestionDiv.style.borderWidth = '1px';
                suggestionDiv.style.borderStyle = 'solid';
                suggestionDiv.textContent = suggestion;
                chatMessages.appendChild(suggestionDiv);
                chatMessages.scrollTop = chatMessages.scrollHeight;
            }, 1200);
        }
    }, 600 + Math.random() * 500);
}

function toggleChat() {
    const chatWindow = document.getElementById('chatWindow');
    const chatToggle = document.getElementById('chatToggle');
    const isOpen = chatWindow.classList.toggle('open');

    const t = translations[currentLang];
    chatToggle.innerHTML = isOpen ?
        '<i class="fas fa-times"></i> <span>' + t.close + '</span>' :
        '<i class="fas fa-comment-dots"></i> <span>' + t.questions + '</span>';

    if (isOpen) {
        document.body.classList.add('no-scroll');
        document.getElementById('chatInput').focus();
        const chatMessages = document.getElementById('chatMessages');

        if (chatMessages.children.length === 0) {
            if (!virtzAI.askedName) {
                const greeting = virtzAI.getGreeting();
                addMessageToChat(greeting);
            } else {
                const greeting = virtzAI.userName ?
                    `Olá ${virtzAI.userName}! Como posso ajudar você hoje? 😊` :
                    'Olá! Como posso ajudar você hoje? 😊';
                addMessageToChat(greeting);

                const tip = '💡 Dica: Pergunte sobre frete, pagamento, trocas ou promoções!';
                const tipDiv = document.createElement('div');
                tipDiv.className = 'bot-msg';
                tipDiv.style.fontSize = '12px';
                tipDiv.style.color = '#888';
                tipDiv.style.background = 'transparent';
                tipDiv.style.border = 'none';
                tipDiv.style.boxShadow = 'none';
                tipDiv.style.padding = '4px 0';
                tipDiv.textContent = tip;
                chatMessages.appendChild(tipDiv);
            }
        }
    } else {
        document.body.classList.remove('no-scroll');
    }
}

