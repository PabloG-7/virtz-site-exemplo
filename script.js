function openModal(name, img, price) {
    closeChatIfOpen();
    modalProduct = { name: name, img: img, price: price };
    document.getElementById('modalImg').src = img;
    document.getElementById('modalTitle').textContent = name;
    document.getElementById('modalPrice').textContent = 'R$ ' + price.toFixed(2);

    document.getElementById('modalOverlay').classList.add('active');
    document.body.classList.add('no-scroll');
}

function closeModal() {
    document.getElementById('modalOverlay').classList.remove('active');
    document.body.classList.remove('no-scroll');
}

function addFromModal() {
    addToCart(modalProduct.name, modalProduct.price);
    closeModal();
}

function subscribeNewsletter() {
    const emailInput = document.getElementById('emailInput');
    const email = emailInput.value.trim();

    if (email === '') {
        showToast('Por favor, digite um e-mail válido.');
        return;
    }

    showToast('Obrigado! ' + email + ' agora faz parte da comunidade.');
    emailInput.value = '';
}

function showToast(message) {
    toastMessage.textContent = message;
    toast.classList.add('show');
    clearTimeout(toast._timeout);
    toast._timeout = setTimeout(() => {
        toast.classList.remove('show');
    }, 3000);
}

function toggleMenu() {
    const nav = document.getElementById('navLinks');
    nav.classList.toggle('active');
    const links = nav.querySelectorAll('a');
    links.forEach(link => {
        link.addEventListener('click', () => {
            nav.classList.remove('active');
        });
    });
}

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
        }
    });
}, { threshold: 0.1 });

document.querySelectorAll('.section-title, .product-card, .fav-card').forEach((el) => {
    observer.observe(el);
});

