let cart = [];
let modalProduct = { name: '', img: '', price: 0 };

const cartCounter = document.getElementById('cartCounter');
const toast = document.getElementById('toast');
const toastMessage = document.getElementById('toastMessage');

function closeChatIfOpen() {
    const chatWindow = document.getElementById('chatWindow');
    if (chatWindow.classList.contains('open')) {
        toggleChat();
    }
}

function addToCart(name, price) {
    const existingItem = cart.find(item => item.name === name);
    if (existingItem) {
        existingItem.quantity += 1;
    } else {
        cart.push({ name: name, price: price, quantity: 1 });
    }
    updateCartUI();
    showToast(name + ' foi adicionado ao carrinho!');
}

function removeFromCart(name) {
    cart = cart.filter(item => item.name !== name);
    updateCartUI();
}

function updateCartUI() {
    const cartItemsContainer = document.getElementById('cartItems');
    const cartTotalElement = document.getElementById('cartTotal');

    let totalItems = 0;
    cart.forEach(item => totalItems += item.quantity);
    cartCounter.textContent = totalItems;
    cartCounter.style.transform = 'scale(1.5)';
    setTimeout(() => { cartCounter.style.transform = 'scale(1)'; }, 200);

    cartItemsContainer.innerHTML = '';
    if (cart.length === 0) {
        const t = translations[currentLang];
        cartItemsContainer.innerHTML = `<p class="empty-cart-msg">${t.emptyCart}</p>`;
        cartTotalElement.textContent = 'R$ 0,00';
        return;
    }

    let totalPrice = 0;
    cart.forEach((item) => {
        totalPrice += item.price * item.quantity;

        const itemHTML = `
            <div class="cart-item">
                <img src="${cartImages[item.name] || 'https://images.unsplash.com/photo-1556905055-8f358a7a47b2?w=100'}" class="cart-item-img" alt="${item.name}">
                <div class="cart-item-info">
                    <h4>${item.name}</h4>
                    <p>Qtd: ${item.quantity} - R$ ${(item.price * item.quantity).toFixed(2)}</p>
                </div>
                <span class="cart-item-remove" onclick="removeFromCart('${item.name}')"><i class="fas fa-trash-alt"></i></span>
            </div>
        `;
        cartItemsContainer.innerHTML += itemHTML;
    });

    cartTotalElement.textContent = 'R$ ' + totalPrice.toFixed(2);
}

function openCart() {
    closeChatIfOpen();
    document.getElementById('cartOverlay').classList.add('active');
    document.getElementById('cartSidebar').classList.add('active');
    document.body.classList.add('no-scroll');
}

function closeCart() {
    document.getElementById('cartOverlay').classList.remove('active');
    document.getElementById('cartSidebar').classList.remove('active');
    document.body.classList.remove('no-scroll');
}

function checkout() {
    if (cart.length === 0) {
        showToast('Seu carrinho está vazio!');
        return;
    }
    alert('Obrigado pela compra! Total: ' + document.getElementById('cartTotal').textContent);
    cart = [];
    updateCartUI();
    closeCart();
}

